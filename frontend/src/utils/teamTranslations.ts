/** Returns "🇹🇷 Türkiye" style display string for a team. Falls back to "🏳️ <name>". */
export function teamDisplay(team: string | null | undefined): string {
  if (!team || team.trim() === "") return "Henüz belli değil";
  const entry = TEAM_TRANSLATIONS[team.trim()];
  if (entry) return `${entry[0]} ${entry[1]}`;
  return `🏳️ ${team}`;
}
